b = 6
print(b - 4.5 >= b * 0.2 - 0.3)
'''
b - 4.5 >= 0.2b - 0.3
b - 0.2b >= 4.5 - 0.3
0.8b >= 4.2
8b >= 42 
b >= 42 / 8
b >= 5.25
'''
c = 3
print(c * 2 - 3 > c - 3 + 2)
'''
2c - 3 > c - 1
c > 2 
c = 3
'''

d = 3
print(8 - 2 * d < d + 2)
'''
8 - 2d < d + 2
-3d < -6
d > 2
'''
e = -3
print(4 * (5 + e) <= -2 * (e - 2))
'''
20 + 4e <= -2e + 4
6e <= -16
e <= -16 / 6
e = -3
'''